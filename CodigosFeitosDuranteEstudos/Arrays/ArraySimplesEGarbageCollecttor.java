package CodigosFeitosDuranteEstudos.Arrays;

public class ArraySimplesEGarbageCollecttor {
    public static void main(String[] args) {
        String[] names = new String[4];

        /*
         * Array é uma coelção de elementos de um unico tipo de dado
         * Tem tamanho fixo
         * É do tipo referência, pois sem a referência(index), não funciona corretamente
         * Array != de List
         * Arrays com espações vazio, retornam o valor nulo daquele tipo de array
         *   Ex: int --- 0
         *       String --- null
         */

        names = new String[7]; // Redeclaração de array
    }
}
